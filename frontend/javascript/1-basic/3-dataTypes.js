// wrap value with single or double quote
let firstName = "Usman";
let lastName = 'Lubis';

console.log(firstName);
console.log(lastName);
console.log(typeof(firstName));
console.log(typeof(lastName));

// number
let no1 = 10;
let no2 = -20;
console.log(no1);
console.log(no2);
console.log(typeof(no1));
console.log(typeof(no2));

let fln1 = 1.5;
let fln2 = 3.0;
console.log(fln1);
console.log(fln2);
console.log(typeof(fln1));
console.log(typeof(fln2));

// boolean
let benar = true;
let salah = false;
console.log(benar);
console.log(salah);
console.log(typeof(benar));
console.log(typeof(salah));

// null
let data = null;
console.log(data);
console.log(typeof(data));

// undefined
let kosong = undefined;
let noData;
console.log(kosong);
console.log(noData);
console.log(typeof(kosong));
console.log(typeof(noData));

// array (you can store multiple data with different data type)
let names = ['ronaldo', 'marcelo', 33, 53, null, undefined, false];
console.log(names);
console.log(typeof(names));

// object (have properties and value
let car = {
  name: "Ferrari",
  wheels: 4,
  year: 2015
};

console.log(car);
console.log(typeof(car));
